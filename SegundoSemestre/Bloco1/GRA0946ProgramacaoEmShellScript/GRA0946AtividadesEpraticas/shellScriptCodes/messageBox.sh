#!/bin/bash

#Caixa de menságem interativa simples com o whiptail TUI

whiptail --title "Caixa de Menságem" --msgbox "Criando uma caixa de mensagem com whiptail. Escolha OK para continuar." --fb 10 50

#--msgbox é o tipo da caixa de menságem
#--fb é o botão com sombreado
# 10 numero de linhas
# 50 numero de colunas
